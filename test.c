void draw_text_block(uint8_t screen[128][32], uint8_t x, int16_t y) {
    int i, j;
    for (i = 0; i < 32; i++) {
        for (j = 0; j < 32; j++) {
            screen[i + x][j + y] = game_over_text[i][j];
        }
    }
}

void draw_object(uint8_t screen[128][32], uint8_t x, uint8_t y, uint8_t num) {
    uint8_t start = num * 7;
    int i, j;
    for (i = start; i < start + 7; i++) {
        for (j = 0; j < 6; j++) {
            screen[i - start + x][j + y] = zero[i][j];
        }
    }
}

void draw_number(uint8_t screen[128][32], uint8_t number) {
    if (number > 99) {
        number = 99;
    }
    if (number / 10 < 1) {
        draw_object(screen, 120, 13, number);
    } else {
        draw_object(screen, 120, 10, number / 10);
        draw_object(screen, 120, 16, number % 10);
    } 
}

void matrix_to_display(uint8_t matrix[128][32], uint8_t display[512]) {
    int i, j;

	for (i = 0; i < 128; i++) {
		for (j = 0; j < 32; j++) {
            if (matrix[i][j] == 1) {
                display[i + (j / 8) * 128] |= 1 << (j % 8);
            }
		}
	}
}

void draw_block(uint8_t screen[128][32], uint8_t x, int16_t y, uint8_t width, uint8_t height) {
    int i, j;
    for (i = 0; i < 128; i++) {
        for (j = 0; j < 32; j++) {
            if (i >= x && i < (x + width) && j >= y && j < (y + height)) {
                screen[i][j] = 1;
            }
        }
    }
}

void move_pipe(Pipe_pair * pipe) {
    pipe->y += 1;
    if (pipe->y > 32) {
        pipe->y = -7;
    }
}

void draw_pipe(uint8_t screen[128][32], Pipe_pair *pipe) {
  draw_block(screen, 60, pipe->y, pipe->gap_position, 8);
}

void clear_display(uint8_t screen[128][32]) {
    int i, j;
    for (i = 0; i < 128; i++) {
        for (j = 0; j < 32; j++) {
            screen[i][j] = 0;
        }
    }
}

void print_matrix(uint8_t screen[128][32]) {
    int i, j;
    for (i = 0; i < 128; i++) {
        for (j = 0; j < 32; j++) {
            printf("%d", screen[i][j]);
        }
        printf("\n");
    }
}

void print_display(uint8_t display[512]) {
    int i;
    for (i = 0; i < 512; i++) {
        printf("%d,", display[i]);
    }
    printf("\n");
}

void move_bird(Bird *bird) {
    bird->speed += GRAVITY;
    bird->speed = min(bird->speed, MAX_SPEED);
    bird->x += bird->speed;
}

